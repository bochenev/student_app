  methods: {


    removeSubject: function (groupId, subjectId) {
      return fetch(`${location.origin}/api/v1/group/${groupId}/subjects/${subjectId}`, {
        method: 'DELETE'
      }).then(res => {
        if (res.ok) window.location.reload();
      })
    },

    appendSubject: function (groupId, subjectId) {
      return fetch(`${location.origin}/api/v1/group/${groupId}/subjects/${subjectId}`, {
        method: 'PUT'
      }).then(res => {
        if (res.ok) window.location.reload();
      })
    }

  }
